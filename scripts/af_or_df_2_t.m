clear;
close all;

k = linspace(1, 4);

t_1 = 1/(0.9^5) + 4 ./ (k.*0.9^5 - 0.9^5);

t_2 = 1/(1.8 - 0.5 * 0.9^5) + ...
    1./(0.9^5 - 0.5 * 0.9^5) + ...
    1./(0.9^4 - 0.5 * 0.9^5) + ...
    1./(0.9^3 - 0.5 * 0.9^5) + ...
    1./(0.9^2 - 0.5 * 0.9^5) + 0*k;


t_3 = 0.9 / (1.8 - 0.45) + 3.6 ./ (0.9 - 0.45) + 0*k;

plot(k, t_1);
hold on
plot(k, t_2);
plot(k, t_3);
grid
